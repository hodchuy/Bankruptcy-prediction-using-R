df.altman <- read.csv("t-1 Altman.csv")
scale.altman <- scale(df.altman[1:4])
var(scale.altman[,1])
df.final.altman <- cbind(scale.altman,df.altman[5])
library(caTools)
set.seed(101)
split.altman = sample.split(df.final.altman$br, SplitRatio = 0.7)
train.altman = subset(df.final.altman, split == TRUE)
test.altman = subset(df.final.altman, split == FALSE)
str(train.altman)
library(neuralnet)
nn.altman <- neuralnet(br ~ a + b + c + d , data = train.altman, hidden = 5, linear.output = FALSE)
predicted.nn.altman <- compute(nn.altman, test[,1:4])
predictions.altman <- sapply(predicted.nn.altman$net.result, round)
table(predictions.altman,test.altman$br)
